package com.pas.pojo;

public class OuterWeek 
{
	private String outerWeekDescription;
	private Integer weekNumberStart;
	private Integer weekNumberEnd;
	
	public String getOuterWeekDescription() {
		return outerWeekDescription;
	}
	public void setOuterWeekDescription(String outerWeekDescription) {
		this.outerWeekDescription = outerWeekDescription;
	}
	public Integer getWeekNumberStart() {
		return weekNumberStart;
	}
	public void setWeekNumberStart(Integer weekNumberStart) {
		this.weekNumberStart = weekNumberStart;
	}
	public Integer getWeekNumberEnd() {
		return weekNumberEnd;
	}
	public void setWeekNumberEnd(Integer weekNumberEnd) {
		this.weekNumberEnd = weekNumberEnd;
	}
	
	
}
