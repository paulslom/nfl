package com.pas.pojo;

public class Decade
{
	private String decadeDescription;
	private Integer decadeStart;
	private Integer decadeEnd;
	
	public String getDecadeDescription() {
		return decadeDescription;
	}
	public void setDecadeDescription(String decadeDescription) {
		this.decadeDescription = decadeDescription;
	}
	public Integer getDecadeStart() {
		return decadeStart;
	}
	public void setDecadeStart(Integer decadeStart) {
		this.decadeStart = decadeStart;
	}
	public Integer getDecadeEnd() {
		return decadeEnd;
	}
	public void setDecadeEnd(Integer decadeEnd) {
		this.decadeEnd = decadeEnd;
	}
}
